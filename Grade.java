public class Grade {
    public static void main(String[] args) {
    int Grade=81;
    if(Grade>=90){
        System.out.println("A");
    }
    else if(Grade>=75&&Grade<=89){
      System.out.println("B");
    }
    else if(Grade>=50&&Grade<=74){
        System.out.println("C");
    }
    else{
        System.out.println("Fail");
    }
}
}
